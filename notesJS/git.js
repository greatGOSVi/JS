/*
git commands

git config: configurar tu cuenta de git
git init: para iniciar los comandos de git
git add: enlistar lo que despues subiras a git
git commit: empaquetar y ponerle descripcion al grupo de cambios enlistados con add
git status: muesta el estado de la rama local en la que estes comparandola con la remota
git checkout: cambia a otra rama
git push: sube los cambios a remoto
git pull: actualiza la rama local para que sea como la remota
*/